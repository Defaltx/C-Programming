""" Tuples are a data type that contains more than one item. Once a tuple has been created the contents cannot be changed """



tuple = (1,2,3,"four")

print(tuple)


#Indexing
#Indexing allows the programmer to select an item in the tuple
#Picking the first item in the tuple
print(tuple[0])

#Selecting the last item in the tuple
print(tuple[-1])

#Selecting more than one item in the tuple
print(tuple[1:3])
