""" Lists are a data type that contains more than one item. Unlike the tuple, a list can be manipulated"""

#Indexing
a = [1,2,3,4,5,6,7]

#Printing the first item
print(a[0])

#Printing the last item
print(a[-1])

#Printing every other item
print(a[0:6:2])

#Printing the length of the list
print(len(a))

#Printing the minimum value in the list
print(min(a))

##Printing the maximum value in the list
print(max(a))

#Changing the value of an item in the list
a[3] = 10

